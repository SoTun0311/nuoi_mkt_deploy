let currentLang = 'vi';

// Close dropdown when clicking outside
document.addEventListener('click', function(event) {
    const selector = document.querySelector('.language-selector');
    const dropdown = document.getElementById('langDropdown');
    if (dropdown && !selector.contains(event.target)) {
        dropdown.classList.remove('show');
    }
});

function setLanguage(lang) {
    currentLang = lang;
    const t = translations[lang];
    
    // Close dropdown after selection
    const dropdown = document.getElementById('langDropdown');
    if (dropdown) {
        dropdown.classList.remove('show');
    }
    
    // Update meta tags
    document.title = t.title;
    document.querySelector('meta[name="description"]').content = t.metaDescription;
    document.querySelector('meta[property="og:title"]').content = t.title;
    document.querySelector('meta[property="og:description"]').content = t.metaDescription;
    document.querySelector('meta[name="twitter:title"]').content = t.title;
    document.querySelector('meta[name="twitter:description"]').content = t.metaDescription;
    
    // Update content
    document.querySelector('.logo img').alt = t.logo;
    document.querySelector('h1').innerHTML = t.mainTitle;
    document.querySelector('.slogan').innerHTML = t.slogan;
    
    // Update sections
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (t[key]) {
            el.innerHTML = t[key];
        }
    });
    
    // Update button onclick
    document.querySelector('.cta-button').onclick = () => alert(t.donateAlert);
    
    // Update active language button
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
    });
    document.querySelector(`[data-lang="${lang}"]`).classList.add('active');
    
    // Save preference
    localStorage.setItem('preferredLanguage', lang);
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('preferredLanguage') || 'vi';
    setLanguage(savedLang);
});